#include <stdio.h>

/**
 * add - Adds two integers
 * @a: first integer
 * @b: second integer
 * Returns: addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * subt - Subtracts two integers
 * @a: first integer
 * @b: second integer
 * Returns: subtracted result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - divides two integers
 * @a: first integer
 * @b: second integer
 * Returns: division result
 */
int div(int a, int b)
{
	return (a / b);
}
 /**
 * mod - mod of two integers
 * @a: first integer
 * @b: second integer
 * Returns: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * Returns: final answer
 */
int mul(int a, int b)
{
	return (a * b);
}
